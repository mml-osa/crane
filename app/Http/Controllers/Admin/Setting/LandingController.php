     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function publish(Request $request)
    {
        try {
            $session = Auth::user();
            $CcLanding = CcLanding::first();
            if ($request->switch == 1){$publish = "published";} else{$publish = "unpublished";}
            $CcLanding->update(['state'=>$request->switch,'update_id'=>$session->id]);
            Controller::sessionVariables();
            return redirect()->back()->with("success", "Landing page $publish successfully");
        } catch (\Exception $e) { return redirect()->back()->with("warning", "There was a problem publishing landing page.");}
    }

